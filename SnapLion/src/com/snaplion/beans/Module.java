package com.snaplion.beans;

public class Module {
	public String TabId;
	public String TabCustomName;
	public String ModifiedDate;

	public String getTabId() {
		return TabId;
	}

	public void setTabId(String tabId) {
		TabId = tabId;
	}

	public String getTabCustomName() {
		return TabCustomName;
	}

	public void setTabCustomName(String tabCustomName) {
		TabCustomName = tabCustomName;
	}

	public String getModifiedDate() {
		return ModifiedDate;
	}

	public void setModifiedDate(String modifiedDate) {
		ModifiedDate = modifiedDate;
	}

}
